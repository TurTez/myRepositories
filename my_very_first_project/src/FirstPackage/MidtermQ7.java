package FirstPackage;

/**
 Q7.	Write a switch case where if the value is 5 print "we have reached number 5" and
 exit out of the switch conditional statement.
 **/
public class MidtermQ7 {
    public static void main(String[] args) {
        int num = 7;
        switch (num){
            case 1:
                System.out.println("case 1");
                break;
            case 2:
                System.out.println("Case 2");
                break;
            case 3:
                System.out.println("Case 3");
                break;
            case 4:
                System.out.println("Case 4");
                break;
            case 5:
                System.out.println("We have reached number 5");
            default:
                System.out.println("try number up to 5");
        }
        }
    }


